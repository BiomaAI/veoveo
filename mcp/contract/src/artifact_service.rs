/// Keyset-paginated discovery request for artifacts visible to the caller.
///
/// The cursor is an opaque occurrence identity, not a content hash or storage
/// key. Implementations must still apply the complete artifact policy to every
/// returned item.
#[derive(Debug, Clone, Default, PartialEq, Eq, Serialize, Deserialize, JsonSchema)]
pub struct ListArtifactsRequest {
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub cursor: Option<ArtifactId>,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub limit: Option<u16>,
}

#[derive(Debug, Clone, PartialEq, Serialize, Deserialize, JsonSchema)]
pub struct ArtifactPage {
    pub artifacts: Vec<ArtifactMetadata>,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub next_cursor: Option<ArtifactId>,
}

/// Typed failure surface for the plane. `Denied` carries the [`AccessDecision`]
/// so audit evidence keeps the reason chain (tenant / clearance / need-to-know).
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum ArtifactPlaneError {
    /// No such artifact (or the caller may not learn that it exists).
    NotFound,
    /// Access denied, with the deciding reason for audit.
    Denied(AccessDecision),
    /// The caller presented no usable identity.
    Unauthenticated,
    /// The request was malformed (bad id, URI, labels, or limits).
    InvalidRequest(String),
    /// A conflicting state prevented the mutation.
    Conflict(String),
    /// Transport or backend failure.
    Transport(String),
}

impl std::fmt::Display for ArtifactPlaneError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::NotFound => f.write_str("artifact not found"),
            Self::Denied(d) => write!(f, "access denied: {d:?}"),
            Self::Unauthenticated => f.write_str("unauthenticated"),
            Self::InvalidRequest(m) => write!(f, "invalid request: {m}"),
            Self::Conflict(m) => write!(f, "conflict: {m}"),
            Self::Transport(m) => write!(f, "transport error: {m}"),
        }
    }
}

impl std::error::Error for ArtifactPlaneError {}

/// The interface every domain server programs against to reach the plane.
///
/// Uses native `async fn` in traits (edition 2024). Callers are generic over
/// the concrete plane implementation, so no dynamic dispatch or `async-trait`
/// dependency is needed; tests substitute an in-memory reference PEP.
pub trait ArtifactPlane {
    /// Store `bytes`, stamping tenant/owner from the caller's identity and
    /// recording an owner `Admin` grant. Returns the canonical metadata.
    fn put(
        &self,
        caller: &PlaneCaller,
        request: PutArtifactRequest,
        bytes: Vec<u8>,
    ) -> impl std::future::Future<Output = Result<ArtifactMetadata, ArtifactPlaneError>> + Send;

    /// Fetch bytes + metadata if the caller has at least `level`.
    fn get(
        &self,
        caller: &PlaneCaller,
        artifact_id: &ArtifactId,
        level: AccessLevel,
    ) -> impl std::future::Future<Output = Result<ArtifactObject, ArtifactPlaneError>> + Send;

    /// Metadata only, gated at `Read`.
    fn head(
        &self,
        caller: &PlaneCaller,
        artifact_id: &ArtifactId,
    ) -> impl std::future::Future<Output = Result<ArtifactMetadata, ArtifactPlaneError>> + Send;

    /// Discover metadata for artifacts on which the caller has at least read
    /// access. Results are ordered newest first and keyset-paginated.
    fn list(
        &self,
        caller: &PlaneCaller,
        request: ListArtifactsRequest,
    ) -> impl std::future::Future<Output = Result<ArtifactPage, ArtifactPlaneError>> + Send;

    /// Resolve a neutral `artifact://{id}` plane URI to bytes, gated at `Read`.
    /// This is how a server feeds another server's artifact into its own tool.
    fn resolve(
        &self,
        caller: &PlaneCaller,
        uri: &str,
    ) -> impl std::future::Future<Output = Result<ArtifactObject, ArtifactPlaneError>> + Send;

    /// Add or raise a grant. Requires the caller to hold `Admin` on the artifact.
    fn grant(
        &self,
        caller: &PlaneCaller,
        artifact_id: &ArtifactId,
        subject: AccessSubject,
        level: AccessLevel,
    ) -> impl std::future::Future<Output = Result<(), ArtifactPlaneError>> + Send;

    /// Remove a grant. Requires the caller to hold `Admin` on the artifact.
    fn revoke(
        &self,
        caller: &PlaneCaller,
        artifact_id: &ArtifactId,
        subject: &AccessSubject,
    ) -> impl std::future::Future<Output = Result<(), ArtifactPlaneError>> + Send;

    /// List an artifact's grants. Requires `Admin` on the artifact.
    fn list_grants(
        &self,
        caller: &PlaneCaller,
        artifact_id: &ArtifactId,
    ) -> impl std::future::Future<Output = Result<Vec<Grant>, ArtifactPlaneError>> + Send;

    /// Change whether an artifact may be published. Requires `Admin`.
    fn set_release_state(
        &self,
        caller: &PlaneCaller,
        artifact_id: &ArtifactId,
        release_state: ArtifactReleaseState,
    ) -> impl std::future::Future<Output = Result<ArtifactMetadata, ArtifactPlaneError>> + Send;

    /// Create a read-only anyone-with-link bearer. Requires `Admin` and an
    /// explicitly releasable artifact.
    fn create_share_link(
        &self,
        caller: &PlaneCaller,
        artifact_id: &ArtifactId,
        request: CreateArtifactShareLinkRequest,
    ) -> impl std::future::Future<Output = Result<ArtifactShareLink, ArtifactPlaneError>> + Send;

    /// Revoke a public share link. Requires `Admin` on its artifact.
    fn revoke_share_link(
        &self,
        caller: &PlaneCaller,
        artifact_id: &ArtifactId,
        link_id: &ArtifactShareLinkId,
    ) -> impl std::future::Future<Output = Result<(), ArtifactPlaneError>> + Send;
}

#[cfg(test)]
mod tests {
    use std::collections::BTreeSet;

    use chrono::{TimeDelta, Utc};
    use serde_json::json;

    use super::*;
    use crate::gateway::DataLabelId;

    #[test]
    fn occurrence_and_control_ids_are_uuid_v7() {
        let artifact = ArtifactId::new();
        let capability = ArtifactWriteCapabilityId::new();
        let link = ArtifactShareLinkId::new();

        assert_eq!(artifact.as_uuid().get_version_num(), 7);
        assert_eq!(capability.as_uuid().get_version_num(), 7);
        assert_eq!(link.as_uuid().get_version_num(), 7);
        assert_eq!(ArtifactId::parse(artifact.to_string()).unwrap(), artifact);
        assert!(ArtifactId::parse(uuid::Uuid::new_v4().to_string()).is_err());
        assert!(ArtifactWriteCapabilityId::parse("not-a-uuid").is_err());
    }

    #[test]
    fn artifact_write_idempotency_key_is_validated() {
        let key = ArtifactWriteIdempotencyKey::new("media:task:output:0").unwrap();
        assert_eq!(key.as_str(), "media:task:output:0");
        assert!(ArtifactWriteIdempotencyKey::new("").is_err());
        assert!(ArtifactWriteIdempotencyKey::new(" leading").is_err());
        assert!(ArtifactWriteIdempotencyKey::new("line\nbreak").is_err());
        assert!(ArtifactWriteIdempotencyKey::new("x".repeat(257)).is_err());
    }

    #[test]
    fn effective_labels_union_classification() {
        let classification = DataLabelId::new("cui").unwrap();
        let explicit = DataLabelId::new("us_only").unwrap();
        let request = PutArtifactRequest {
            classification: Some(classification.clone()),
            data_labels: BTreeSet::from([explicit.clone()]),
            metadata: json!({"producer": "test"}),
            ..Default::default()
        };
        assert_eq!(
            request.effective_labels(),
            BTreeSet::from([classification, explicit])
        );
    }

    #[test]
    fn capability_secret_is_validated_and_redacted() {
        assert!(ArtifactWriteCapabilitySecret::new("short").is_err());
        let secret = ArtifactWriteCapabilitySecret::new("s".repeat(32)).unwrap();
        assert_eq!(
            format!("{secret:?}"),
            "ArtifactWriteCapabilitySecret(<redacted>)"
        );
        let issued = IssuedArtifactWriteCapability {
            capability_id: ArtifactWriteCapabilityId::new(),
            secret,
            task_id: "task-1".into(),
            expires_at: Utc::now() + TimeDelta::minutes(5),
        };
        assert!(!format!("{issued:?}").contains(&"s".repeat(32)));
    }

    #[test]
    fn share_link_defaults_do_not_assert_limits() {
        let request = CreateArtifactShareLinkRequest::default();
        assert!(request.expires_at.is_none());
        assert!(request.max_downloads.is_none());
        let value = serde_json::to_value(request).unwrap();
        assert_eq!(value, json!({}));
    }

    #[test]
    fn share_link_debug_redacts_the_bearer_url() {
        let token = "public-share-token-that-must-not-be-logged";
        let link = ArtifactShareLink {
            link_id: ArtifactShareLinkId::new(),
            artifact_id: ArtifactId::new(),
            url: format!("https://veoveo.example/s/{token}"),
            expires_at: Utc::now() + TimeDelta::minutes(5),
            max_downloads: None,
        };
        let diagnostic = format!("{link:?}");
        assert!(!diagnostic.contains(token));
        assert!(diagnostic.contains("<redacted>"));
    }
}
