macro_rules! artifact_uuid_id {
    ($name:ident, $label:literal) => {
        #[derive(
            Clone,
            Copy,
            Debug,
            PartialEq,
            Eq,
            PartialOrd,
            Ord,
            Hash,
            Serialize,
            Deserialize,
            JsonSchema,
        )]
        #[serde(try_from = "String", into = "String")]
        pub struct $name(uuid::Uuid);

        impl $name {
            pub fn new() -> Self {
                Self(uuid::Uuid::now_v7())
            }

            pub fn parse(value: impl AsRef<str>) -> Result<Self, ArtifactPlaneError> {
                let value = uuid::Uuid::parse_str(value.as_ref()).map_err(|_| {
                    ArtifactPlaneError::InvalidRequest(concat!($label, " must be a UUIDv7").into())
                })?;
                if value.get_version_num() != 7 {
                    return Err(ArtifactPlaneError::InvalidRequest(
                        concat!($label, " must be a UUIDv7").into(),
                    ));
                }
                Ok(Self(value))
            }

            pub const fn as_uuid(self) -> uuid::Uuid {
                self.0
            }
        }

        impl Default for $name {
            fn default() -> Self {
                Self::new()
            }
        }

        impl fmt::Display for $name {
            fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
                self.0.fmt(f)
            }
        }

        impl TryFrom<String> for $name {
            type Error = ArtifactPlaneError;

            fn try_from(value: String) -> Result<Self, Self::Error> {
                Self::parse(value)
            }
        }

        impl From<$name> for String {
            fn from(value: $name) -> Self {
                value.to_string()
            }
        }
    };
}

artifact_uuid_id!(ArtifactWriteCapabilityId, "artifact write capability id");

/// Stable caller-selected identity for one asynchronous artifact write.
///
/// Retrying a capability redemption with the same key returns the UUIDv7
/// occurrence reserved by the first successful attempt. Before any occurrence
/// is staged, a retry may rebind that reservation to regenerated bytes; after
/// staging or finalization, retries always return the original occurrence.
#[derive(Debug, Clone, Eq, Hash, Ord, PartialEq, PartialOrd, Serialize, JsonSchema)]
#[serde(transparent)]
pub struct ArtifactWriteIdempotencyKey(String);

impl ArtifactWriteIdempotencyKey {
    pub fn new(value: impl Into<String>) -> Result<Self, ArtifactPlaneError> {
        let value = value.into();
        if value.is_empty()
            || value.len() > 256
            || value.chars().any(char::is_control)
            || value.trim() != value
        {
            return Err(ArtifactPlaneError::InvalidRequest(
                "artifact write idempotency key must be 1..=256 trimmed, non-control characters"
                    .into(),
            ));
        }
        Ok(Self(value))
    }

    pub fn as_str(&self) -> &str {
        &self.0
    }
}

impl fmt::Display for ArtifactWriteIdempotencyKey {
    fn fmt(&self, formatter: &mut fmt::Formatter<'_>) -> fmt::Result {
        formatter.write_str(&self.0)
    }
}

impl<'de> Deserialize<'de> for ArtifactWriteIdempotencyKey {
    fn deserialize<D>(deserializer: D) -> Result<Self, D::Error>
    where
        D: serde::Deserializer<'de>,
    {
        Self::new(String::deserialize(deserializer)?).map_err(serde::de::Error::custom)
    }
}
artifact_uuid_id!(ArtifactShareLinkId, "artifact share link id");

/// Opaque identity of an artifact-write capability issued by the artifact
/// service. This is not an artifact id or a gateway identity assertion.
/// Bearer secret returned once by the artifact service. The issuing artifact
/// service stores only a one-way hash; the authorized domain server retains
/// the secret with the task that needs it after asynchronous completion.
#[derive(Clone, PartialEq, Eq, Serialize, JsonSchema)]
#[serde(transparent)]
pub struct ArtifactWriteCapabilitySecret(String);

impl ArtifactWriteCapabilitySecret {
    pub fn new(value: impl Into<String>) -> Result<Self, ArtifactPlaneError> {
        let value = value.into();
        if value.len() < 32 || value.chars().any(char::is_whitespace) {
            return Err(ArtifactPlaneError::InvalidRequest(
                "artifact write capability secret must be at least 32 non-whitespace characters"
                    .to_string(),
            ));
        }
        Ok(Self(value))
    }

    pub fn expose_secret(&self) -> &str {
        &self.0
    }
}

impl fmt::Debug for ArtifactWriteCapabilitySecret {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        f.write_str("ArtifactWriteCapabilitySecret(<redacted>)")
    }
}

impl<'de> Deserialize<'de> for ArtifactWriteCapabilitySecret {
    fn deserialize<D>(deserializer: D) -> Result<Self, D::Error>
    where
        D: serde::Deserializer<'de>,
    {
        let value = String::deserialize(deserializer)?;
        Self::new(value).map_err(serde::de::Error::custom)
    }
}

